package REPO.DSA.JAVA.Lec_7;

public class relationalOperator {
    public static void main(String[] args) {
        
        // Relational operator compares value and gives result in boolean true or false
        // ==	Equal to
        // !=	Not equal to
        // >	Greater than
        // <	Less than
        // >=	Greater than or equal
        // <=	Less than or equal

        int a = 45;
        int b = 50;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
