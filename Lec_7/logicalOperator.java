package REPO.DSA.JAVA.Lec_7;

public class logicalOperator {
    public static void main(String[] args) {
        
        // Logical operators are applied on True/False , conditions
        // &&	Logical AND
        // ||   Logical OR
        // !	Logical NOT             Reverse the bit (boolean) value

        boolean a = true;
        boolean b = false;

        System.out.println(a && b);
        System.out.println(a || b);
        System.out.println(!b);


        // Precedence of Logical AND is more than Logical OR

        // && > ||
        
    }
}
