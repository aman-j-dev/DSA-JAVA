package REPO.DSA.JAVA.Lec_7;

public class assignmentOperator {
    public static void main(String[] args) {
        
        // Assignment operator is used to assign values

        int a = 5;

        System.out.println("Value of a : " + a);

        a += 5;

        System.out.println("Value of a : " + a);
        
        a -= 5;
        
        System.out.println("Value of a : " + a);
        
        a *= 10;
        
        System.out.println("Value of a : " + a);
        
        a /= 10;
        
        System.out.println("Value of a : " + a);


    }
}
